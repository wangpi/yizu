                                        2，网络正常，也没有屏蔽措施。访问该链接查看dns设置是否正常    <a href="http://tools.cloudxns.net/index.php/Index/Diag" target="_blank">http://tools.cloudxns.net/index.php/Index/Diag</a><br>
